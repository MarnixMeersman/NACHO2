def string_to_month(string):
    if string == 'Jan': return 1
    elif string == 'Feb': return 2
    elif string == 'Mar': return 3
    elif string == 'Apr': return 4
    elif string == 'May': return 5
    elif string == 'Jun': return 6
    elif string == 'Jul': return 7
    elif string == 'Aug': return 8
    elif string == 'Sep': return 9
    elif string == 'Oct': return 10
    elif string == 'Nov': return 11
    elif string == 'Dec': return 12

